public boolean checkUsername(String srcode, String password) {
        PreparedStatement ps;
        ResultSet rs;
        boolean existingUser = false;

        String query = "SELECT * FROM admin_info WHERE adminSR = ?, adminPW = ?";
        try {
            ps = Myconnection.getConnection().prepareStatement(query);
            ps.setString(1, srcode);
            ps.setString(2, password);
            rs = ps.executeQuery();

            if (rs.next()) {
                existingUser = true;
                JOptionPane.showMessageDialog(null, "This SR-Code already exists", "Use another SR-Code.",
                        JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Myconnection.class.getName()).log(Level.SEVERE, null, ex);
        }
        return existingUser;
    }

