    private loginCredentials getAuthenticatedUser(String sr, String pw) {
        loginCredentials user = null;

        final String DB_URL = "jdbc:mysql://localhost/finalprojectdb?serverTimezone=UTC";
        final String USERNAME = "root";
        final String PASSWORD = "";

        try {
            Connection conn = DriverManager.getConnection(DB_URL, USERNAME, PASSWORD);


            Statement stmt = conn.createStatement();
            String sql = "SELECT * FROM admin_info WHERE adminSR=? AND adminPW=?";
            PreparedStatement preparedStatement = conn.prepareStatement(sql);
            preparedStatement.setString(1, sr);
            preparedStatement.setString(2, pw);
            ResultSet resultSet = preparedStatement.executeQuery();

            if (resultSet.next()) {
                user = new loginCredentials();
                user.sr = resultSet.getString("adminSR");
                user.pw = resultSet.getString("adminPW");
            }
            stmt.close();
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }

        return user;
    }
