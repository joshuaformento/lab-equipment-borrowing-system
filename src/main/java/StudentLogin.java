    private void signBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signBtnActionPerformed
        String sr = srcodeTF.getText();
    String password = new String(passwordTF.getPassword());

    if (verifyField()) {
        if (!checkUsername(sr)) {
            PreparedStatement ps;
            String register = "INSERT INTO student_info (SRCODE, PASSWORD) VALUES (?, ?)";

            try {
                ps = con.prepareStatement(register);
                ps.setString(1, sr);
                ps.setString(2, password);

                if (ps.executeUpdate() != 0) {
                    JOptionPane.showMessageDialog(null, "Account has been added");
                }

            } catch (SQLException ex) {
                throw new RuntimeException(ex);
            }
            srcodeTF.setText("");
            passwordTF.setText(""); // Clear password field after sign-up
        }
    }
    }//GEN-LAST:event_signBtnActionPerformed
