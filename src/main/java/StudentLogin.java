    private loginCredentials getAuthenticatedUser(String sr, String password) {
        loginCredentials user = null;

    final String DB_URL = "jdbc:mysql://localhost/finalprojectdb?serverTimezone=Asia/Manila";
    final String USERNAME = "root";
    final String PASSWORD = "";

    try {
        Connection conn = DriverManager.getConnection(DB_URL, USERNAME, PASSWORD);

        String sql = "SELECT * FROM student_info WHERE SRCODE=? AND PASSWORD=?";
        PreparedStatement preparedStatement = conn.prepareStatement(sql);
        preparedStatement.setString(1, sr);
        preparedStatement.setString(2, password);

        ResultSet resultSet = preparedStatement.executeQuery();

        if (resultSet.next()) {
            user = new loginCredentials();
            user.sr = resultSet.getString("SRCODE");
        }

        conn.close();
    } catch (Exception e) {
        e.printStackTrace();
    }

    return user;
    }

